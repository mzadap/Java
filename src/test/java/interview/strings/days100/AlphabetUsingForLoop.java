package interview.strings.days100;

public class AlphabetUsingForLoop {

    /*
    *23. Print all alphabet using for loop Program in
java
    * */
    public static void main(String[] args) {
        char ch;
        for (ch = 'a'; ch<= 'z'; ch++) {
            System.out.print(ch + " ");
        }
    }
}
